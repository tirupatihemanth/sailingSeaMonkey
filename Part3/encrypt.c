void aRound(byte *state, byte *rKey){ 
    int i;
    shiftRows(state);
    byte temp[2] = {0x00, 0x00}; 
    temp[0] = temp[1] = sbox[state[0]];
    temp[0]^=Te1[state[1]][0];
    temp[1]^=Te1[state[1]][1];
    memcpy(state, temp, BYTE_SIZE*2);

    temp[0] = temp[1] = sbox[state[2]];
    temp[0]^=Te1[state[3]][0];
    temp[1]^=Te1[state[3]][1];
    memcpy(state+2, temp, BYTE_SIZE*2);
    xorState(state, rKey);

}


int main(int argc, char **argv) {
    if(argc!=4){
        printf("Usage: ./encrypt inputFile keyFile encryptedFile\n");
    }
    int i;
    FILE *key = fopen(argv[2], "r");
    if(!key || fread(mKey, BYTE_SIZE, KEY_SIZE, key)!=KEY_SIZE){
        printf("KEY_FILE_NOT_FOUND || INVALID_KEY. EXITiNG...\n");
        exit(0);
    }
    fclose(key);
    byte rKey[NUM_ROUNDS+1][BLOCK_SIZE];
    rKeyGen(mKey, rKey);
    FILE *in = fopen(argv[1], "r");
    FILE *out = fopen(argv[3], "w");
    if(!in || !out){
        printf("FILE_IO_ERROR. EXITING...\n");
        exit(0);
    }
    byte block[BLOCK_SIZE];
    short bytesRead;
    byte tail=BLOCK_SIZE;
    while((bytesRead = fread(block, BYTE_SIZE, BLOCK_SIZE, in))>0){
        //Padding if necessary with spaces
        if(bytesRead<BLOCK_SIZE){
            for(i=bytesRead;i<BLOCK_SIZE;i++){
                block[i]=0x20;
            }
            tail = bytesRead;
        }
        //Using 0 as an initialization vector
        //Because state state vector has initial value 0
        xorState(block, state);
        memcpy(state, block, BLOCK_SIZE);
        xorState(state, rKey[0]);
        for(i=0;i<NUM_ROUNDS;i++){
            aRound(state, rKey[i+1]);
        }
        fwrite(state, BYTE_SIZE, BLOCK_SIZE, out);
    }

    block[0]= tail;
    xorState(block,state);
    xorState(block, rKey[0]);
    for(i=0;i<NUM_ROUNDS;i++){
        aRound(block, rKey[i+1]);
    }
    fwrite(block, BYTE_SIZE, BLOCK_SIZE, out);

    fclose(in);
    fclose(out);
    return 0;
}
